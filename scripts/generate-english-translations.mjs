import fs from 'node:fs/promises';
import path from 'node:path';
import ts from 'typescript';

const root = process.cwd();
const srcRoot = path.join(root, 'src');
const outputPath = path.join(srcRoot, 'i18n.generated.ts');
const cachePath = path.join(root, 'scripts', '.english-translations-cache.json');

const normalize = (value) => value.replace(/\s+/g, ' ').trim();
const decodeEntities = (value) => value
  .replaceAll('&amp;', '&')
  .replaceAll('&apos;', "'")
  .replaceAll('&#39;', "'")
  .replaceAll('&quot;', '"')
  .replaceAll('&lt;', '<')
  .replaceAll('&gt;', '>');

async function sourceFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const nested = await Promise.all(entries.map(async (entry) => {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) return sourceFiles(fullPath);
    return /\.(ts|tsx)$/.test(entry.name) && entry.name !== 'i18n.generated.ts' ? [fullPath] : [];
  }));
  return nested.flat();
}

function looksUserFacing(value) {
  if (!/[A-Za-zÀ-ÿ]/.test(value) || value.length < 2) return false;
  if (/^(?:\/[a-z0-9_./:-]+|#[a-f0-9]+)$/i.test(value)) return false;
  if (/^(?:flex|grid|block|inline|hidden|relative|absolute|fixed|sticky|rounded|border|bg-|text-|px-|py-|pt-|pb-|pl-|pr-|mt-|mb-|ml-|mr-|gap-|min-|max-|w-|h-|items-|justify-|font-|leading-|tracking-|transition|duration|hover:|focus:|active:|dark:)/.test(value)) return false;
  if (/^(?:button|page|status|alert|polite|dialog|navigation|main|none|auto|system|light|dark|success|error|checking|persistent|unsupported|default|granted|denied|replace|merge|open|done|lesson)$/i.test(value)) return false;
  if (/^[a-z]+(?:[A-Z][A-Za-z0-9]*)+$/.test(value)) return false;
  return true;
}

function collectText(filePath, code, values) {
  const source = ts.createSourceFile(filePath, code, ts.ScriptTarget.Latest, true, filePath.endsWith('x') ? ts.ScriptKind.TSX : ts.ScriptKind.TS);
  const add = (raw) => {
    const value = normalize(raw);
    if (looksUserFacing(value)) values.add(value);
    const decoded = normalize(decodeEntities(raw));
    if (decoded !== value && looksUserFacing(decoded)) values.add(decoded);
  };
  const visit = (node) => {
    if (ts.isStringLiteral(node) || ts.isNoSubstitutionTemplateLiteral(node)) add(node.text);
    if (ts.isJsxText(node)) add(node.getText(source));
    ts.forEachChild(node, visit);
  };
  visit(source);
}

async function translateDutch(text) {
  const protectedText = text.replaceAll('Koers', '__KOERS_APP__');
  const url = new URL('https://translate.googleapis.com/translate_a/single');
  url.searchParams.set('client', 'gtx');
  url.searchParams.set('sl', 'nl');
  url.searchParams.set('tl', 'en');
  url.searchParams.set('dt', 't');
  url.searchParams.set('q', protectedText);
  const response = await fetch(url, { headers: { 'User-Agent': 'Koers translation generator' } });
  if (!response.ok) throw new Error(`Translation failed (${response.status}) for: ${text.slice(0, 80)}`);
  const body = await response.json();
  return normalize(body[0].map((part) => part[0]).join('')).replaceAll('__KOERS_APP__', 'Koers');
}

const values = new Set();
for (const filePath of await sourceFiles(srcRoot)) {
  collectText(filePath, await fs.readFile(filePath, 'utf8'), values);
}

let cache = {};
try {
  cache = JSON.parse(await fs.readFile(cachePath, 'utf8'));
} catch {
  // A cache is optional on the first run.
}

const pending = [...values].filter((value) => !cache[value]);
let cursor = 0;
const workers = Array.from({ length: 10 }, async () => {
  while (cursor < pending.length) {
    const index = cursor++;
    const source = pending[index];
    cache[source] = await translateDutch(source);
    if (index % 50 === 0) process.stdout.write(`Translated ${index + 1}/${pending.length}\n`);
  }
});
await Promise.all(workers);

await fs.writeFile(cachePath, `${JSON.stringify(cache, null, 2)}\n`, 'utf8');
const polish = (source, translated) => source.includes('Koers')
  ? translated.replace(/\b(?:Course|Rate)\b/g, 'Koers')
  : translated;
const ordered = Object.fromEntries(
  [...values].sort((a, b) => a.localeCompare(b, 'nl')).map((key) => [key, polish(key, cache[key])])
);
const output = `// Generated by scripts/generate-english-translations.mjs.\n` +
  `// Keep this file committed so English remains fully offline.\n` +
  `export const ENGLISH_TRANSLATIONS: Readonly<Record<string, string>> = ${JSON.stringify(ordered, null, 2)};\n`;
await fs.writeFile(outputPath, output, 'utf8');
process.stdout.write(`Wrote ${Object.keys(ordered).length} translations to ${path.relative(root, outputPath)}\n`);
